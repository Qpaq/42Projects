int		ft_sqrt(int nb)
{
	int		x;
	int		i;

	i = 0;
	x = 1;
	while (i <= (nb / 2))
	{
		if (x * x == nb)
			return (x);
		x = (x + nb / x) / 2;
		i++;
	}
	return (0);
}
